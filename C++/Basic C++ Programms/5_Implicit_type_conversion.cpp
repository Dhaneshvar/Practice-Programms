#include<iostream>

using namespace std;

int main(){
	//Implicit type Conversion
	int num1 = 'a';
	int num2 = 'A';
	char ch1 = 65;
	int num3 = 123.123;
	float f = 1342;
	cout<<"num1 "<<num1<<endl;
	cout<<"num2 "<<num2<<endl;
	cout<<"ch1 "<<ch1<<endl;
	cout<<"num3 "<<num3<<endl;
	cout<<"float "<<f<<endl;
}
/*
num1 97
num2 65
ch1 A
num3 123
float 1342
*/
